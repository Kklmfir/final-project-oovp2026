    private void loadTable() {
        try {
            String sql = "SELECT Member_ID, Member_Name, Member_Type_ID, Identifier, Email, Phone, Register_Date FROM Member";
            PreparedStatement pst = conn.prepareStatement(sql);
            ResultSet rs = pst.executeQuery();
            tblMember.setModel(DbUtils.resultSetToTableModel(rs));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }
    
    private void autoIDMember() {
    try {
        String sql = "SELECT MAX(Member_ID) FROM member";
        PreparedStatement pst = conn.prepareStatement(sql);
        ResultSet rs = pst.executeQuery();
        
        if (rs.next()) {
            String maxID = rs.getString(1);
            if (maxID == null) {
                txtMemberID.setText("M001");
            } else {
                int id = Integer.parseInt(maxID.substring(1)) + 1;
                txtMemberID.setText(String.format("M%03d", id));
            }
        }
        txtMemberID.setEditable(false);
        
    } catch (Exception e) {
        System.out.println("Error AutoID Member: " + e.getMessage());
    }
}

