    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        try {
            String sql = "INSERT INTO Member (Member_ID, Member_Name, Member_Type_ID, Identifier, Email, Phone) VALUES (?, ?, ?, ?, ?, ?)";
            PreparedStatement pst = conn.prepareStatement(sql);

            pst.setString(1, txtMemberID.getText());
            pst.setString(2, txtMemberName.getText());

            String typeValue = cbMemberType.getSelectedItem().toString();
            String typeID = typeValue.split("-")[0]; 
            pst.setString(3, typeID);

            pst.setString(4, cbIdentifier.getSelectedItem().toString());
            pst.setString(5, txtEmail.getText());
            pst.setString(6, txtPhone.getText());

            pst.executeUpdate();
            JOptionPane.showMessageDialog(null, "Member berhasil didaftarkan!");
            autoIDMember();
            loadTable();

            txtMemberID.setText("");
            txtMemberName.setText("");
            cbIdentifier.setSelectedIndex(0);
            txtEmail.setText("");
            txtPhone.setText("");  
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
        }
    }//GEN-LAST:event_btnAddActionPerformed

    private void tblMemberMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblMemberMouseClicked
        try {
            int baris = tblMember.getSelectedRow();
    
            txtMemberID.setText(tblMember.getValueAt(baris, 0).toString());
            txtMemberName.setText(tblMember.getValueAt(baris, 1).toString());
    
            String typeID = tblMember.getValueAt(baris, 2).toString();
    for (int i = 0; i < cbMemberType.getItemCount(); i++) {
        if (cbMemberType.getItemAt(i).toString().startsWith(typeID)) {
            cbMemberType.setSelectedIndex(i);
            break;
        }
    }
    
            String identifierVal = tblMember.getValueAt(baris, 3) != null ? tblMember.getValueAt(baris, 3).toString() : "Student";

            cbIdentifier.setSelectedItem(identifierVal);
            txtEmail.setText(tblMember.getValueAt(baris, 4) != null ? tblMember.getValueAt(baris, 4).toString() : "");
            txtPhone.setText(tblMember.getValueAt(baris, 5) != null ? tblMember.getValueAt(baris, 5).toString() : "");
    
        } catch (Exception e) {
            System.out.println("Error klik tabel: " + e.getMessage());
        }
    }//GEN-LAST:event_tblMemberMouseClicked

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        try {
            String sql = "UPDATE Member SET Member_Name=?, Member_Type_ID=?, Email=?, Phone=?, Identifier=? WHERE Member_ID=?";
            PreparedStatement pst = conn.prepareStatement(sql);

            pst.setString(1, txtMemberName.getText());

            String typeValue = cbMemberType.getSelectedItem().toString();
            String typeID = typeValue.split("-")[0]; 
            pst.setString(2, typeID);

            pst.setString(3, txtEmail.getText());
            pst.setString(4, txtPhone.getText());
            pst.setString(5, cbIdentifier.getSelectedItem().toString());

            pst.setString(6, txtMemberID.getText());

            pst.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data Member berhasil diubah!");

            loadTable(); 

            txtMemberID.setText("");
            txtMemberName.setText("");
            txtEmail.setText("");
            txtPhone.setText("");
            cbIdentifier.setSelectedIndex(0);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Error Update: " + e.getMessage());
            }
    }//GEN-LAST:event_btnUpdateActionPerformed

