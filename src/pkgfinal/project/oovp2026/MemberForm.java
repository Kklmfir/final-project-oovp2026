    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // Cek dulu apakah ID sudah terisi (apakah ada data yang dipilih)
        if(txtMemberID.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Silakan klik data di tabel terlebih dahulu untuk dihapus!");
            return; // Menghentikan proses jika kosong
        }

        int confirm = JOptionPane.showConfirmDialog(null, "Yakin ingin menghapus data member ini?", "Konfirmasi Hapus", JOptionPane.YES_NO_OPTION);

        if (confirm == JOptionPane.YES_OPTION) {
            try {
                String sql = "DELETE FROM Member WHERE Member_ID=?";
                PreparedStatement pst = conn.prepareStatement(sql);
                pst.setString(1, txtMemberID.getText());
                pst.executeUpdate();
                JOptionPane.showMessageDialog(null, "Data Member berhasil dihapus!");
                loadTable(); 

                txtMemberID.setText("");
                txtMemberName.setText("");
                txtEmail.setText("");
                txtPhone.setText("");
                cbIdentifier.setSelectedIndex(0);
                cbMemberType.setSelectedIndex(0);
        
             } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Error Delete: " + e.getMessage());
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

